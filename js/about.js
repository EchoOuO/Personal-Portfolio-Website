function toggleParagraph() {
    const paragraph = document.querySelector('.faq-detail');

    // Toggle the display property
    if (paragraph.style.display === 'none') {
        paragraph.style.display === 'block';
    } else {
        paragraph.style.display === 'none';
    }
};


